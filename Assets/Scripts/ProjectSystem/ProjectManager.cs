public class ProjectManager : Singleton<ProjectManager>
{
    private RuntimeProject openProject;
    public MenuManager menuManager;
    public Menu newProjectMenu;
    public bool projectIsOpen;

    private void Awake()
    {
        Instance = this;
        projectIsOpen = false;
